package velo.importer;



public class RoleResourceAttributeUnit {

	String resourceAttributeName;
	String roleResourceAttributeValue;
	String roleResourceAttributeType;
	boolean isToBeCreated;
	/**
	 * @return the resourceAttributeName
	 */
	public String getResourceAttributeName() {
		return resourceAttributeName;
	}
	/**
	 * @param resourceAttributeName the resourceAttributeName to set
	 */
	public void setResourceAttributeName(String resourceAttributeName) {
		this.resourceAttributeName = resourceAttributeName;
	}
	/**
	 * @return the roleResourceAttributeValue
	 */
	public String getRoleResourceAttributeValue() {
		return roleResourceAttributeValue;
	}
	/**
	 * @param roleResourceAttributeValue the roleResourceAttributeValue to set
	 */
	public void setRoleResourceAttributeValue(String roleResourceAttributeValue) {
		this.roleResourceAttributeValue = roleResourceAttributeValue;
	}
	/**
	 * @return the roleResourceAttributeType
	 */
	public String getRoleResourceAttributeType() {
		return roleResourceAttributeType;
	}
	/**
	 * @param roleResourceAttributeType the roleResourceAttributeType to set
	 */
	public void setRoleResourceAttributeType(String roleResourceAttributeType) {
		this.roleResourceAttributeType = roleResourceAttributeType;
	}
	/**
	 * @return the isToBeCreated
	 */
	public boolean getIsToBeCreated() {
		return isToBeCreated;
	}
	/**
	 * @param isToBeCreated the isToBeCreated to set
	 */
	public void setIsToBeCreated(boolean isToBeCreated) {
		this.isToBeCreated = isToBeCreated;
	}
	
	
}
